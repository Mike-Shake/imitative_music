package com.example.imitative_music.find;

public class tuijian_Bean {
    private String tuijianming;
    private int tuijiantu;

    public tuijian_Bean(String tuijianming, int tuijiantu) {
        this.tuijianming = tuijianming;
        this.tuijiantu = tuijiantu;
    }

    public String getTuijianming() {
        return tuijianming;
    }

    public int getTuijiantu() {
        return tuijiantu;
    }
}
